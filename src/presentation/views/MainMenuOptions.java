package presentation.views;

/**
 * Options that can be used in the menu, including:
 *
 * {@link #CHARACTER_CREATION}
 * {@link #LIST_CHARACTERS}
 * {@link #CREATE_ADVENTURE}
 * {@link #START_ADVENTURE}
 *
 * @author Sebastián Félix Gorga & Valèria Ezquerra Rodriguez
 * @version 1.0
 */
public enum MainMenuOptions {
    /**
     * Option that lets the user create and add a new character to the system
     */
    CHARACTER_CREATION,

    /**
     * Option that lets the user list the characters
     */
    LIST_CHARACTERS,

    /**
     * Option that lets the user create an adventure
     */
    CREATE_ADVENTURE,

    /**
     * Option that lets the user start an adventure
     */
    START_ADVENTURE,

    /**
     * Option that says goodbye to the user
     */
    EXIT
}

